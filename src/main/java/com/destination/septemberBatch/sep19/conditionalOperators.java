package com.destination.septemberBatch.sep19;

public class conditionalOperators {
	public static void main(String[] args) {
		System.out.println("===== Conditional Operator =====");
		int a=100 , b=50;
		System.out.println("Value No.1 = "+a);
		System.out.println("Value No.2 = "+b);
		System.out.println("==================================");
		boolean res1=a>b;
		System.out.println("Greater than result is "+res1);
		System.out.println("==================================");
		boolean res2=a<b;
		System.out.println("Lesser than result is "+res2);
		System.out.println("============================");
		boolean res3=a<=b;
		System.out.println("Lesser than or equal to result is "+res3);
		System.out.println("=====================================");
		boolean res4=a>=b;
		System.out.println("Greater than or equal to result is "+res4);
	    System.out.println("======================================");
	    boolean res5=a==b;
	    System.out.println("Equals to result is "+res5);
	    System.out.println("======================================");
	    boolean res6=a!=b;
	    System.out.println("Not equals to result is "+res6);
	    System.out.println("========================================");
		
	}

}
