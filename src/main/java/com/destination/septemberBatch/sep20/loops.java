package com.destination.septemberBatch.sep20;

public class loops {
	public static void main(String[] args) {
		System.out.println("Example of for loop");
		System.out.println("==========================");
		for(int i=1;i<=10;i++) {
			System.out.println("tommorow we have backup class @ 10am");
		}
		System.out.println("=====================================");
		System.out.println("example of while loop");
		System.out.println("====================================");
		int i=1;
	    while(i<=10) {
	    	System.out.println("tommorow we have backup class @ 11am");
	    	i++;
	    }
	    System.out.println("==========================================");
	    System.out.println("example of do...while loop");
	    System.out.println("=========================================");
	    int j=1;
	    do {
	    	System.out.println("tommorow we have backup class @ 12am");
	    	j++;
	    }while(j<-10);
	}

}
